package com.lid.outfitplannerbackend.model;


public class Colors {

  private long id;
  private String name;
  private long R;
  private long G;
  private long B;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public long getR() {
    return R;
  }

  public void setR(long R) {
    this.R = R;
  }


  public long getG() {
    return G;
  }

  public void setG(long G) {
    this.G = G;
  }


  public long getB() {
    return B;
  }

  public void setB(long B) {
    this.B = B;
  }

}
